import { Router } from "express";

import { authMiddleware } from "../../middleware/auth.middleware";

import {
  getComments,
  createComment,
  updateComment,
  deleteComment,
  createReply,
} from "./comment.controller";

const router = Router();

//////////////////////////////////////////////////
// GET COMMENTS
//////////////////////////////////////////////////

router.get(
  "/mod/:modId",
  getComments
);

//////////////////////////////////////////////////
// CREATE COMMENT
//////////////////////////////////////////////////

router.post(
  "/mod/:modId",
  authMiddleware,
  createComment
);

//////////////////////////////////////////////////
// EDIT COMMENT
//////////////////////////////////////////////////

router.patch(
  "/:id",
  authMiddleware,
  updateComment
);

//////////////////////////////////////////////////
// DELATE COMMENT
//////////////////////////////////////////////////

router.delete(
  "/:id",
  authMiddleware,
  deleteComment
);

//////////////////////////////////////////////////
// 
//////////////////////////////////////////////////

router.post(
  "/:id/replies",
  authMiddleware,
  createReply
);

export default router;